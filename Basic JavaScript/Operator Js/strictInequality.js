/**
 * Strict inequality 
 * checks whether its two operands are not equal, 
 * returning a Boolean result. Unlike the inequality 
 * operator, the strict inequality operator always considers operands 
 * of different types to be different.
 */

console.log(1 !== 1); //expected output: false

console.log('hello' !== 'hello'); //expected output: false

console.log('1' !== 1); //expected output : true