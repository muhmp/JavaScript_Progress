/**
 * Use dot notation to access properties of an object
 */
//example
let duck = { 
    name: "donald",
    numLegs: 2
  };
  console.log(duck.name);

  //another example
  let dog = {
    name: "Spot",
    numLegs: 4
  };
console.log(dog.name); // first access the object name then the property
console.log(dog.numLegs); // first access the object name 